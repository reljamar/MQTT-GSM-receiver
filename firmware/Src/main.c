void send_AT_command(char *command) {
    char command_with_termination[BUFFER_SIZE];
    memset(command_with_termination, 0, BUFFER_SIZE);
    snprintf(command_with_termination, BUFFER_SIZE, "%s\r\n", command);
    HAL_UART_Transmit(&huart1, (uint8_t*)command_with_termination, strlen(command_with_termination), HAL_MAX_DELAY);
    snprintf(command_with_termination, BUFFER_SIZE, "Me: %s\r\n", command);
    HAL_UART_Transmit(&huart2, (uint8_t*)command_with_termination, strlen(command_with_termination), HAL_MAX_DELAY); //send a copy of the command also to PC

}

int wait_for_response(char *response) {
    char rx_buffer[BUFFER_SIZE];
    memset(rx_buffer, 0, BUFFER_SIZE);
    HAL_UART_Receive(&huart1, (uint8_t*)rx_buffer, BUFFER_SIZE, 1500);

    char command_with_termination[BUFFER_SIZE];
    snprintf(command_with_termination, BUFFER_SIZE, "Quecky: %s", rx_buffer);
    HAL_UART_Transmit(&huart2, (uint8_t*)command_with_termination, strlen(command_with_termination), HAL_MAX_DELAY); //send a copy of the command also to PC


    if (strstr(rx_buffer, response) != NULL) {
        return 1; // OK received
    }
    return 0; // OK not received
}

